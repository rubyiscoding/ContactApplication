import Foundation

class Contacts{
    var firstName: String
    var lastName: String
    var phoneNumber: String
    var email: String

    
    init(firstName: String, lastName: String, phoneNumber: String, email: String) {
        self.firstName = firstName
        self.lastName = lastName
        self.phoneNumber = phoneNumber
        self.email = email
    }
}


